                        </div>
                        <p>Corinne Lanssens débute la danse avec Marie‑Pierre Bianco, puis poursuit sa formation au Conservatoire National de Région de Toulouse avec
                            Jean‑Pierre Guérin, à l’Opéra National de Paris, et au Conservatoire National Supérieur de Paris avec Christiane Vaussard.</p>
                        <p>À 17 ans, elle est engagée au Jeune Ballet de France sous la direction de Robert Berthier, puis rejoint le Ballet du Nord sous
                            la direction d’Alfonso Cata. Elle poursuit sa carrière à l’étranger, au Théâtre National de Bern en Suisse, puis au Théâtre
                            National de Gräz en Autriche.</p>
                        <p>En 1993, elle intègre le Ballet de l’Opéra National de Bordeaux, où elle dansera sous les directions successives de
                            Paolo Bortoluzzi, Éric Vu‑An et Charles Jude. Elle y travaillera avec de nombreux chorégraphes de renom jusqu’à la fin de sa carrière.</p>
                        <p>En 2013, après 25 années de scène dont vingt à Bordeaux, elle se retire pour se consacrer à l’enseignement.
                            Diplômée d’État depuis 2016, elle est co‑directrice de la Jude Mikhalev Ballet Academy de 2019 à 2025.</p>
                        <p>Aujourd’hui, elle est co‑fondatrice et co‑directrice de la Mikhalev Lanssens Ballet Academy aux côtés de son époux,
                            Roman Mikhalev, et continue d’y enseigner la danse classique.</p>
